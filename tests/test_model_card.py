class TestGenerateModelCardFormat:
    """Tests for output format correctness."""

    def test_output_is_string(self) -> None:
        """Output is a string."""
        card = generate_model_card(name="m")
        assert isinstance(card, str)

    def test_lora_rank_in_overview(self) -> None:
        """LoRA rank appears in overview when in config."""
        card = generate_model_card(name="m", config={"lora_r": 16})
        assert "LoRA Rank" in card
        assert "16" in card

    def test_quantization_in_overview(self) -> None:
        """Quantization appears in overview when in config."""
        card = generate_model_card(name="m", config={"quantization": "4bit"})
        assert "Quantization" in card
        assert "4bit" in card

    def test_dataset_private_keys_excluded(self) -> None:
        """Dataset keys starting with '_' are excluded."""
        card = generate_model_card(
            name="m",
            dataset_info={"name": "ds", "_internal": "secret"},
        )
        assert "_internal" not in card
        assert "secret" not in card

    def test_float_metrics_formatted(self) -> None:
        """Float metrics are formatted to 4 decimal places."""
        card = generate_model_card(name="m", metrics={"loss": 0.123456789})
        assert "0.1235" in card

    def test_non_float_metrics_shown_as_is(self) -> None:
        """Non-float metrics are shown without formatting."""
        card = generate_model_card(name="m", metrics={"status": "complete"})
        assert "complete" in card
