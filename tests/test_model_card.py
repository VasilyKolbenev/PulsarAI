"""Tests for llm_forge.model_card module.

Tests generate_model_card() with various field combinations,
required sections, custom extra_sections, and minimal inputs.
"""

import pytest

from llm_forge.model_card import generate_model_card


class TestGenerateModelCardAllFields:
    """Tests for generate_model_card() with all fields provided."""

    def test_header_contains_model_name(self) -> None:
        """Card header includes the model name."""
        card = generate_model_card(name="my-model")
        assert "# Model Card: my-model" in card

    def test_contains_generated_timestamp(self) -> None:
        """Card contains a generated-on timestamp."""
        card = generate_model_card(name="m")
        assert "Generated on" in card

    def test_overview_contains_base_model(self) -> None:
        """Overview table includes base model when provided."""
        card = generate_model_card(name="m", base_model="llama-3-8b")
        assert "llama-3-8b" in card

    def test_overview_contains_task(self) -> None:
        """Overview table includes task in uppercase."""
        card = generate_model_card(name="m", task="dpo")
        assert "DPO" in card

    def test_training_config_section(self) -> None:
        """Training Configuration section is rendered with config params."""
        card = generate_model_card(
            name="m",
            config={"lr": 2e-5, "epochs": 3, "batch_size": 4},
        )
        assert "## Training Configuration" in card
        assert "lr" in card
        assert "epochs" in card
        assert "batch_size" in card

    def test_dataset_section(self) -> None:
        """Training Data section shows dataset info."""
        card = generate_model_card(
            name="m",
            dataset_info={"name": "my_dataset", "rows": 10000},
        )
        assert "## Training Data" in card
        assert "my_dataset" in card

    def test_metrics_section(self) -> None:
        """Evaluation Metrics section shows metric values."""
        card = generate_model_card(
            name="m",
            metrics={"accuracy": 0.95, "f1": 0.92},
        )
        assert "## Evaluation Metrics" in card
        assert "accuracy" in card
        assert "0.9500" in card

    def test_environment_section(self) -> None:
        """Training Environment section shows environment info."""
        card = generate_model_card(
            name="m",
            environment={"gpu": "A100", "framework": "pytorch"},
        )
        assert "## Training Environment" in card
        assert "A100" in card

    def test_environment_packages_excluded(self) -> None:
        """'packages' key in environment is excluded from output."""
        card = generate_model_card(
            name="m",
            environment={"gpu": "A100", "packages": ["torch", "transformers"]},
        )
        assert "packages" not in card.split("## Training Environment")[1].split("##")[0]

    def test_intended_use_custom(self) -> None:
        """Custom intended_use text appears in card."""
        card = generate_model_card(name="m", intended_use="For research only.")
        assert "For research only." in card

    def test_limitations_custom(self) -> None:
        """Custom limitations text appears in card."""
        card = generate_model_card(name="m", limitations="Not for production.")
        assert "Not for production." in card

    def test_extra_sections_appended(self) -> None:
        """Custom extra_sections are appended with correct titles."""
        card = generate_model_card(
            name="m",
            extra_sections={
                "Ethical Considerations": "Be careful with bias.",
                "Deployment Notes": "Use behind an API gateway.",
            },
        )
        assert "## Ethical Considerations" in card
        assert "Be careful with bias." in card
        assert "## Deployment Notes" in card

    def test_footer_present(self) -> None:
        """Footer with 'Generated by LLM Forge' is present."""
        card = generate_model_card(name="m")
        assert "Generated by LLM Forge" in card


